// FIX (HIGH): Добавлен тип ClientShotLogEntry и поле shotLog в SyncStateEvent.
// До этого SYNC_STATE не содержал историю выстрелов, и при реконнекте
// пользователь видел пустую панель истории независимо от прогресса игры.

import type { Board, Coordinate, GamePhase, Missile } from "./types";

// ── Morse primitives ──────────────────────────────────────────────────────────

export type MorseSymbol = "." | "-";

export type MorseSequence = MorseSymbol[];

// ── Event-type registry ───────────────────────────────────────────────────────

export const GameEventType = {
  JOIN_ROOM: "JOIN_ROOM",
  SHIPS_PLACED: "SHIPS_PLACED",
  ATTACK_PREP: "ATTACK_PREP",
  MISSILE_LAUNCHED: "MISSILE_LAUNCHED",
  INTERCEPT_ATTEMPT: "INTERCEPT_ATTEMPT",
  PLAYER_JOINED: "PLAYER_JOINED",
  GAME_STARTED: "GAME_STARTED",
  INCOMING_MISSILE: "INCOMING_MISSILE",
  RESOLVE_HIT: "RESOLVE_HIT",
  SYNC_STATE: "SYNC_STATE",
  ERROR: "ERROR",
} as const;

export type GameEventType = (typeof GameEventType)[keyof typeof GameEventType];

// ── Per-event payload types ───────────────────────────────────────────────────

export type JoinRoomEvent = {
  type: typeof GameEventType.JOIN_ROOM;
  payload: {
    playerId: string;
    playerName: string;
  };
};

export type ShipsPlacedEvent = {
  type: typeof GameEventType.SHIPS_PLACED;
  payload: {
    ships: ReadonlyArray<{ coords: readonly Coordinate[] }>;
  };
};

export type AttackPrepEvent = {
  type: typeof GameEventType.ATTACK_PREP;
  payload: {
    /** The cell the attacker intends to fire at. */
    target: Coordinate;
    /**
     * Stable UUID generated by the attacker.
     * Sent here so the server can lock it in PendingAttack; the same ID must
     * be echoed back in the subsequent MISSILE_LAUNCHED event.
     */
    missileId: string;
  };
};

export type MissileLaunchedEvent = {
  type: typeof GameEventType.MISSILE_LAUNCHED;
  payload: {
    missileId: string;
    target: Coordinate;
    morseSequence: MorseSequence;
    timestamp: number;
  };
};

export type InterceptAttemptEvent = {
  type: typeof GameEventType.INTERCEPT_ATTEMPT;
  payload: {
    missileId: string;
    decodedCoord: Coordinate;
    attemptNumber: number;
  };
};

export type PlayerJoinedEvent = {
  type: typeof GameEventType.PLAYER_JOINED;
  payload: {
    playerId: string;
    playerName: string;
    playerCount: 1 | 2;
  };
};

export type GameStartedEvent = {
  type: typeof GameEventType.GAME_STARTED;
  payload: {
    firstTurnPlayerId: string;
  };
};

export type IncomingMissileEvent = {
  type: typeof GameEventType.INCOMING_MISSILE;
  payload: {
    missileId: string;
    morseSequence: MorseSequence;
    timestamp: number;
    maxAttempts: number;
  };
};

export type HitResult = "hit" | "miss" | "sunk";

export type ResolveHitEvent = {
  type: typeof GameEventType.RESOLVE_HIT;
  payload: {
    missileId: string;
    target: Coordinate;
    result: HitResult;
    nextTurnPlayerId: string;
    isGameOver: boolean;
    winnerId?: string | undefined;
    defenderDecodedCorrectly: boolean;
  };
};

/**
 * Запись об одном выстреле в истории ходов (перспектива получателя SYNC_STATE).
 * Формируется сервером — `by` уже содержит правильную сторону для конкретного игрока.
 */
export type ClientShotLogEntry = {
  by: "us" | "them";
  /** Читаемая координата, например "АБВ-5" */
  coord: string;
  result: HitResult;
  ts: number;
};

export type SyncStateEvent = {
  type: typeof GameEventType.SYNC_STATE;
  payload: {
    phase: GamePhase;
    ownBoard: Board;
    enemyBoard: Board;
    activeMissiles: Missile[];
    isMyTurn: boolean;
    winnerId?: string | undefined;
    /**
     * История выстрелов с точки зрения получателя.
     * Всегда присутствует (пустой массив [] если ходов ещё не было).
     * Используется для восстановления панели истории при реконнекте.
     */
    shotLog: ClientShotLogEntry[];
  };
};

export type ErrorEvent = {
  type: typeof GameEventType.ERROR;
  payload: {
    code: ErrorCode;
    message: string;
  };
};

// ── Error codes ───────────────────────────────────────────────────────────────

export const ErrorCode = {
  ROOM_FULL: "ROOM_FULL",
  INVALID_PLACEMENT: "INVALID_PLACEMENT",
  NOT_YOUR_TURN: "NOT_YOUR_TURN",
  CELL_ALREADY_SHOT: "CELL_ALREADY_SHOT",
  MORSE_MISMATCH: "MORSE_MISMATCH",
  INVALID_COORDINATE: "INVALID_COORDINATE",
  MAX_ATTEMPTS_REACHED: "MAX_ATTEMPTS_REACHED",
  GAME_NOT_STARTED: "GAME_NOT_STARTED",
  INTERNAL: "INTERNAL",
} as const;

export type ErrorCode = (typeof ErrorCode)[keyof typeof ErrorCode];

// ── Discriminated unions ──────────────────────────────────────────────────────

export type ClientGameEvent =
  | JoinRoomEvent
  | ShipsPlacedEvent
  | AttackPrepEvent
  | MissileLaunchedEvent
  | InterceptAttemptEvent;

export type ServerGameEvent =
  | PlayerJoinedEvent
  | GameStartedEvent
  | IncomingMissileEvent
  | ResolveHitEvent
  | SyncStateEvent
  | ErrorEvent;

export type GameEvent = ClientGameEvent | ServerGameEvent;